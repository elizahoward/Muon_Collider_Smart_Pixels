def readAllEvalRes(baseDir,savePath = "./selectedEvaluationResults.csv",doPrint=False):
    allEvalDf = pd.DataFrame()
    allEvalDfList = []
    for e in os.scandir(baseDir):
        singleFilePath = os.path.join(e.path,"evaluation_results.json")
        if Path(singleFilePath).is_file():
            df, _ = readSingleJson(singleFilePath,doPrint=doPrint)
            allEvalDfList.append(df)
        else:
            print("couldn't find",singleFilePath)

    allEvalDf = pd.concat(allEvalDfList)
    allEvalDf.sort_values(by="evalFolderName",inplace = True)
    if doPrint:
        print(allEvalDf)
    allEvalDf.to_csv(savePath, index=False)
    print(f"saved allEvalDfList to {savePath}")
    return allEvalDf


